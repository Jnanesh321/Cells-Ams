// ==================== HELPER FUNCTIONS ====================

/**
 * Get all faculty members
 */
export const getAllFaculty = (): VCETFaculty[] => {
  return Object.values(VCET_DEPARTMENTS).flatMap((dept) => dept.faculty);
};

/**
 * Get faculty by ID
 */
export const getFacultyById = (id: string): VCETFaculty | undefined => {
  return getAllFaculty().find((faculty) => faculty.id === id);
};

/**
 * Get HOD for a department
 */
export const getHODForDepartment = (department: Department): VCETFaculty => {
  return VCET_DEPARTMENTS[department].hod;
};

/**
 * Get all faculty in a department (excluding HOD from list)
 */
export const getFacultyByDepartment = (department: Department): VCETFaculty[] => {
  return VCET_DEPARTMENTS[department].faculty;
};

/**
 * Get department info
 */
export const getDepartmentInfo = (department: Department): VCETDepartment => {
  return VCET_DEPARTMENTS[department];
};

/**
 * Get total college faculty count
 */
export const getTotalFacultyCount = (): number => {
  return getAllFaculty().length;
};

/**
 * Search faculty by name
 */
export const searchFacultyByName = (query: string): VCETFaculty[] => {
  const normalizedQuery = query.toLowerCase();
  return getAllFaculty().filter((faculty) =>
    faculty.name.toLowerCase().includes(normalizedQuery)
  );
};

/**
 * Get faculty count by department
 */
export const getFacultyCountByDepartment = (): Record<Department, number> => {
  return {
    CSE: VCET_DEPARTMENTS.CSE.faculty.length,
    MCA: VCET_DEPARTMENTS.MCA.faculty.length,
    MECH: VCET_DEPARTMENTS.MECH.faculty.length,
    ECE: VCET_DEPARTMENTS.ECE.faculty.length,
    ISE: VCET_DEPARTMENTS.ISE.faculty.length,
    SCIENCE_HUMANITIES: VCET_DEPARTMENTS.SCIENCE_HUMANITIES.faculty.length,
  };
};

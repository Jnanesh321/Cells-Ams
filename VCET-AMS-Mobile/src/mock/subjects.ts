export function getCoreSubjects(department: string, semester: number): VTUSubject[] {
  return VTU_SUBJECTS_2022.filter(
    (s) => s.department === department && s.semester === semester && s.type === 'CORE'
  );
}

export function getElectiveSubjects(department: string, semester: number): VTUSubject[] {
  return VTU_SUBJECTS_2022.filter(
    (s) => s.department === department && s.semester === semester && s.type === 'ELECTIVE'
  );
}

export function getLabSubjects(department: string, semester: number): VTUSubject[] {
  return VTU_SUBJECTS_2022.filter(
    (s) => s.department === department && s.semester === semester && s.type === 'LAB'
  );
}

export function getSemesterSubjects(department: string, semester: number): VTUSubject[] {
  return VTU_SUBJECTS_2022.filter(
    (s) => s.department === department && s.semester === semester
  );
}

export function getSubjectByCode(code: string): VTUSubject | undefined {
  return VTU_SUBJECTS_2022.find((s) => s.code === code);
}

export function getAllSchemes(): string[] {
  return [...new Set(VTU_SUBJECTS_2022.map((s) => s.scheme))];
}
